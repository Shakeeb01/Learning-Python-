# Docstring 
# Python docstrings are the string literals that appear right after the definition ofa function,
# method,class,or module.

def squar(n):
    '''Takes in a number n, returns 
    the square of n'''
    print(n**2)
    
squar(5)    
#this is to print doc string.
print(squar.__doc__)


